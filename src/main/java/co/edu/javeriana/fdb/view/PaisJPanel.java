    protected void refresh(){
        List<PaisDTO> Pais = this.principal.findAllPais();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        int F= jTable1.getRowCount();
        for(int i = 0; F>i;i++)
            model.removeRow(0);

        if(Pais != null)
            for(PaisDTO Pa: Pais)
                model.addRow(new Object[]{Pa.getNombre(),Pa.getIva(),Pa.getImpcon(),Pa.getInTur()});
    }

    private Boolean WhiteSpace (){
        return(this.BoxNombre.getText().equals("") || this.BoxImpuCon.getText().equals("") || this.BoxImpuTu.getText().equals("") || this.BoxIVA.getText().equals(""));
    }
