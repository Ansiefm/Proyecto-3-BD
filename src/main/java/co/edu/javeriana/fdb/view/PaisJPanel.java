    private void BotonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonCrearActionPerformed
        PaisDTO Pais;
        if(!WhiteSpace()){
            Pais = new PaisDTO(this.BoxNombre.getText(), Float.parseFloat(this.BoxImpuCon.getText()),Float.parseFloat(this.BoxImpuTu.getText()),Float.parseFloat(this.BoxIVA.getText()));
            if(this.principal.CrearPais(Pais)) JOptionPane.showMessageDialog(null, "Pais creado!");
            else JOptionPane.showMessageDialog(null,"Ningun campo puede estar vacio!");
            refresh();
        }
    }//GEN-LAST:event_BotonCrearActionPerformed

    private void BotonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEditActionPerformed
        PaisDTO Pais;
        if(!WhiteSpace()){
            Pais = new PaisDTO(this.BoxNombre.getText(), Float.parseFloat(this.BoxImpuCon.getText()),Float.parseFloat(this.BoxImpuTu.getText()),Float.parseFloat(this.BoxIVA.getText()));
            if(this.principal.editPais(Pais))
                JOptionPane.showMessageDialog(null, "Pais actualizado!");
            else JOptionPane.showMessageDialog(null,"No se actualizo!");
            refresh();
        }
    }//GEN-LAST:event_BotonEditActionPerformed

    private void BotonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEliminarActionPerformed
        if(this.BoxNombre.getText().equals(""))
            JOptionPane.showMessageDialog(null,"El campo nombre no puede estar vacio");
            else
                if(this.principal.eliminarPais(this.BoxNombre.getText()))
                    JOptionPane.showMessageDialog(null, "Pais eliminado!");
                else
                JOptionPane.showMessageDialog(null, "No se pudo eliminar el pais. Verifique que existe");
            refresh();
    }//GEN-LAST:event_BotonEliminarActionPerformed

    private void BotonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonBuscarActionPerformed
        PaisDTO Pais;
        if(this.BoxNombre.getText().equals("")) refresh();
        else{
            Pais = this.principal.findPais(this.BoxNombre.getText());
            DefaultTableModel model  = (DefaultTableModel) jTable1.getModel();
            int F = jTable1.getRowCount();
            for(int i =0; F>i;i++)
                model.removeRow(0);

            if(Pais != null)
                model.addRow(new Object[]{Pais.getNombre(),Pais.getImpcon(),Pais.getInTur(),Pais.getIva()});
            else
                JOptionPane.showMessageDialog(null, "No se encontro el pais buscado!");
        }
    }//GEN-LAST:event_BotonBuscarActionPerformed

