    private void BotonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonCrearActionPerformed
        DepartamentoDTO Dep;
        if(!WhiteSpace()){
            Dep = new DepartamentoDTO(this.BoxCode.getText(),this.BoxNombre.getText(),Float.parseFloat(this.BoxPresupuesto.getText()));
            if(this.principal.EditDep(Dep)){
                JOptionPane.showMessageDialog(null,"Departamento creado correctamente!");
            }else{
                JOptionPane.showMessageDialog(null,"Ningun campo debe estar vacio!");
            }
            refresh();
        }
    }//GEN-LAST:event_BotonCrearActionPerformed

    private void BotonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEliminarActionPerformed
        if(this.BoxCode.getText().equals("")){
            JOptionPane.showMessageDialog(null,"El campo del codigo no puede estar vacio!");
        }else{
            if(this.principal.EliminarDep(this.BoxCode.getText()))
                JOptionPane.showMessageDialog(null,"Departamento eliminado!");
            else
            JOptionPane.showMessageDialog(null,"No se pudo eliminar el departamento. Verifique que existe!");
        }
        refresh();
    }//GEN-LAST:event_BotonEliminarActionPerformed

    private void BotonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonBuscarActionPerformed
        DepartamentoDTO Dep;
        if(this.BoxCode.getText().equals("")){
            refresh();
        }else{
            Dep = new DepartamentoDTO(this.BoxCode.getText(),this.BoxNombre.getText(),Float.parseFloat(this.BoxPresupuesto.getText()));
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int F= jTable1.getRowCount();
            for(int i=0;F>i;i++){
                model.removeRow(0);
            }
            if(Dep != null)
                model.addRow(new Object[]{Dep.getCodedep(),Dep.getNombredep(),Dep.getPresupuesto()});
            else
                JOptionPane.showMessageDialog(null, "No se enontro el departamento buscado!");
        }
    }//GEN-LAST:event_BotonBuscarActionPerformed
    private Boolean WhiteSpace(){
        return(this.BoxCode.getText().equals("") || this.BoxNombre.getText().equals("") || this.BoxPresupuesto.getText().equals(""));
    }
    protected void refresh(){
        List<DepartamentoDTO> Dep = this.principal.findAllDep();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        int F=jTable1.getRowCount();
        for(int i = 0; F>i; i++)
            model.removeRow(0);
        if (Dep != null)
            for (DepartamentoDTO De: Dep)
                model.addRow(new Object[]{De.getCodedep(),De.getNombredep(),De.getPresupuesto()});
    }

