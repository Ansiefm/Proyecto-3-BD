    private void BotonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEditActionPerformed
        DepartamentoDTO Depa;
        if(!WhiteSpace()){
            Depa = new DepartamentoDTO(this.BoxCode.getText(),this.BoxNombre.getText(),Float.parseFloat(this.BoxPresupuesto.getText()));
            if(this.principal.CrearDep(Depa)){
                JOptionPane.showMessageDialog(null,"Departamento actualizado!");
            }else{
                JOptionPane.showMessageDialog(null,"No se actualizo!");
            }
            refresh();
        }
    }//GEN-LAST:event_BotonEditActionPerformed

